            pst.executeUpdate();
            pst=con.prepareStatement("UPDATE customer_choice SET payment_confirmation='"+code+"' WHERE customer_id='"+a+"'");
            pst.executeUpdate();

            

            JOptionPane.showMessageDialog(null,"Thank you for your trust !");

