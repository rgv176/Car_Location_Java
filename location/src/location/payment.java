            Connecter();
            String n=txtName.getText();
            String number=txtCard.getText();
            String date=txtDate.getText();
            String c=txtCVV.getText();
            int a=Integer.parseInt(txtId.getText());
            
            Random ran=new Random();
            int code=ran.nextInt(1000000);
